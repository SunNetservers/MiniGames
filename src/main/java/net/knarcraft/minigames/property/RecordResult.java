/**
 * A representation of all possible record results
 */
public enum RecordResult {

    /**
     * No record was achieved
     */
    NONE,

    /**
     * A personal bes was achieved
     */
    PERSONAL_BEST,

    /**
     * A world record was achieved
     */
    WORLD_RECORD

}
